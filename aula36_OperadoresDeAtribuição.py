'''

Operadores de Atribuição
= += -= *= /= //= **= %=

Lembrando:
** -> Potenciação
%  -> Módulo

'''

# Exemplos:

contador = 0
contador += 1 # contador = contador + 1
print(contador)


contador_str = '0'
contador_str += '1' # contador_str = contador_str + '1'
print(contador_str)


contador_teste = 10
contador_teste *= '2' # contador_teste = contador_teste * '2'
print(contador_teste)